    public TumCariler(String s) {
        initComponents();
        seciliisyeriadi = s;
        dtm.setColumnIdentifiers(new Object[]{"TıcariUnvan","Adi","Soyadi","CepTel","TcKimlik"});
        try {

            Connection con = DriverManager.getConnection(url, user, pass);
            Statement st = con.createStatement();
            String sql = "SELECT * FROM Isyeri WHERE Adi = '"+seciliisyeriadi+"'";
            ResultSet rs = st.executeQuery(sql);
            while(rs.next()){
                seciliisyeriid = rs.getInt("Isyeri_id");
                
            }
            Statement st2 = con.createStatement();
            String sql2 = "SELECT * FROM Cari WHERE Isyeri_id ="+seciliisyeriid;
            ResultSet rs2 = st2.executeQuery(sql2);
            while(rs2.next()){
               Vector vector = new Vector();
               vector.add(rs2.getString("TicariUnvan"));
               vector.add(rs2.getString("Adi"));
               vector.add(rs2.getString("Soyadi"));
               vector.add(rs2.getString("CepTel"));
               vector.add(rs2.getString("TcKimlik"));
               dtm.addRow(vector);
                
            }
            con.close();
        } catch (SQLException ex) {
            Logger.getLogger(TumCariler.class.getName()).log(Level.SEVERE, null, ex);
        }
        jTable1.setModel(dtm);

    }

